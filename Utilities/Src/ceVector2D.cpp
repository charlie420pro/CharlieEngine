#include "ceVector2D.h"

namespace ceEngineSDK
{
	/************************************************************************************************************************/
	/* Implementación de funciones del vector                               												*/
	/************************************************************************************************************************/
	 ceVector2D::ceVector2D()
	{

	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D::ceVector2D(float InX, float InY) : X(InX), Y(InY)
	{

	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	ceVector2D::~ceVector2D()
	{
	
	}

	/************************************************************************************************************************/
	/* Implementación de operadores aritméticos                             												*/
	/************************************************************************************************************************/

	 ceVector2D ceVector2D::operator+(const ceVector2D& V) const
	{
		return ceVector2D(X + V.X, Y + V.Y);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D ceVector2D::operator-(const ceVector2D& V) const
	{
		return ceVector2D(X - V.X, Y - V.Y);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D ceVector2D::operator*(float Scale) const
	{
		return ceVector2D(X * Scale, Y * Scale);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D ceVector2D::operator*(const ceVector2D& V) const
	{
		return ceVector2D(X * V.X, Y * V.Y);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D ceVector2D::operator/(float Scale) const
	{
		const float RScale = 1.f / Scale;
		return ceVector2D(X * RScale, Y * RScale);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D ceVector2D::operator/(const ceVector2D& V) const
	{
		return ceVector2D(X / V.X, Y / V.Y);
	}



	/************************************************************************************************************************/
	/* Implementación de operadores lógicos                                 												*/
	/************************************************************************************************************************/
	 bool ceVector2D::operator==(const ceVector2D& V) const
	{
		return X == V.X && Y == V.Y;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector2D::operator!=(const ceVector2D& V) const
	{
		return X != V.X || Y != V.Y;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector2D::operator<(const ceVector2D& Other) const
	{
		return X < Other.X && Y < Other.Y;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector2D::operator>(const ceVector2D& Other) const
	{
		return X > Other.X && Y > Other.Y;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector2D::operator<=(const ceVector2D& Other) const
	{
		return X <= Other.X && Y <= Other.Y;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector2D::operator>=(const ceVector2D& Other) const
	{
		return X >= Other.X && Y >= Other.Y;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector2D::Equals(const ceVector2D& V, float Tolerance) const
	{
		return ceMath::Abs(X - V.X) < Tolerance && ceMath::Abs(Y - V.Y) < Tolerance;
	}

	/************************************************************************************************************************/
	/* Implementación de operadores de asignación compuesta                 												*/
	/************************************************************************************************************************/
	 ceVector2D& ceVector2D::operator+=(const ceVector2D& V)
	{
		X += V.X; Y += V.Y;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D& ceVector2D::operator-=(const ceVector2D& V)
	{
		X -= V.X; Y -= V.Y;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D& ceVector2D::operator*=(float Scale)
	{
		X *= Scale; Y *= Scale;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D& ceVector2D::operator/=(float V)
	{
		const float RV = 1.f / V;
		X *= RV; Y *= RV;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D& ceVector2D::operator*=(const ceVector2D& V)
	{
		X *= V.X; Y *= V.Y;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D& ceVector2D::operator/=(const ceVector2D& V)
	{
		X /= V.X; Y /= V.Y;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 float ceVector2D::Magnitud(const ceVector2D & V) 
	{
		return ceMath::Sqrt(ceMath::Pow(V.X,2) + ceMath::Pow(V.Y,2));
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector2D ceVector2D::Normalize(const ceVector2D & V)
	{
		return V / Magnitud(V);
	}

   /************************************************************************/
   /*                                                                      */
   /************************************************************************/
	 float ceVector2D::operator|(const ceVector2D& V) const
	{
		return X*V.X + Y*V.Y;
	}

	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 float ceVector2D::operator^(const ceVector2D& V) const
	{
		return X*V.Y - Y*V.X;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 float ceVector2D::DotProduct(const ceVector2D& A, const ceVector2D& B)
	{
		return A | B;
	}

}