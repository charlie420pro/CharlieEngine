	/************************************************************************************************************************/
	/* Implementación de funciones del vector                               												*/
	/************************************************************************************************************************/
	 ceVector3D::ceVector3D()
	{

	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D::ceVector3D(float InX, float InY, float InZ) : X(InX), Y(InY), Z(InZ)
	{

	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	ceVector3D::~ceVector3D()
	{

	}

	/************************************************************************************************************************/
	/* Implementación de operadores aritméticos                             												*/
	/************************************************************************************************************************/

	 ceVector3D ceVector3D::operator+(const ceVector3D& V) const
	{
		return ceVector3D(X + V.X, Y + V.Y, Z + V.Z);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator-(const ceVector3D& V) const
	{
		return ceVector3D(X - V.X, Y - V.Y, Z - V.Z);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator*(float Scale) const
	{
		return ceVector3D(X * Scale, Y * Scale, Z * Scale);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator*(const ceVector3D& V) const
	{
		return ceVector3D(X * V.X, Y * V.Y, Z * V.Z);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator/(float Scale) const
	{
		const float RScale = 1.f / Scale;
		return ceVector3D(X * RScale, Y * RScale, Z * RScale);
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator/(const ceVector3D& V) const
	{
		return ceVector3D(X / V.X, Y / V.Y, Z /V.Z);
	}



	/************************************************************************************************************************/
	/* Implementación de operadores lógicos                                 												*/
	/************************************************************************************************************************/
	 bool ceVector3D::operator==(const ceVector3D& V) const
	{
		return X == V.X && Y == V.Y && Z == V.Z;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector3D::operator!=(const ceVector3D& V) const
	{
		return X != V.X || Y != V.Y || Z != V.Z;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector3D::operator<(const ceVector3D& Other) const
	{
		return X < Other.X && Y < Other.Y && Z < Other.Z;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector3D::operator>(const ceVector3D& Other) const
	{
		return X > Other.X && Y > Other.Y && Z > Other.Z;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector3D::operator<=(const ceVector3D& Other) const
	{
		return X <= Other.X && Y <= Other.Y && Z <= Other.Z;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector3D::operator>=(const ceVector3D& Other) const
	{
		return X >= Other.X && Y >= Other.Y && Z >= Other.Z;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 bool ceVector3D::Equals(const ceVector3D& V, float Tolerance) const
	{
		return ceMath::Abs(X - V.X) < Tolerance && ceMath::Abs(Y - V.Y) < Tolerance && ceMath::Abs(Z - V.Z) < Tolerance;
	}

	/************************************************************************************************************************/
	/* Implementación de operadores de asignación compuesta                 												*/
	/************************************************************************************************************************/
	 ceVector3D ceVector3D::operator+=(const ceVector3D& V)
	{
		X += V.X; Y += V.Y; Z += V.Z;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator-=(const ceVector3D& V)
	{
		X -= V.X; Y -= V.Y; Z -= V.Z;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator*=(float Scale)
	{
		X *= Scale; Y *= Scale; Z *= Scale;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator/=(float V)
	{
		const float RV = 1.f / V;
		X *= RV; Y *= RV; Z *= RV;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator*=(const ceVector3D& V)
	{
		X *= V.X; Y *= V.Y; Z *= V.Z;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::operator/=(const ceVector3D& V)
	{
		X /= V.X; Y /= V.Y; Z /= V.Z;
		return *this;
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 float ceVector3D::Magnitud(const ceVector3D & V)
	{
		return ceMath::Sqrt(ceMath::Pow(V.X, 2) + ceMath::Pow(V.Y, 2)+ ceMath::Pow(V.Z,2));
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 ceVector3D ceVector3D::Normalize(const ceVector3D & V)
	{
		return V / Magnitud(V);
	}

	/************************************************************************/
	/* Producto Punto                                                       */
	/************************************************************************/
	 float ceVector3D::operator|(const ceVector3D& V) const
	{
		return X*V.X + Y*V.Y + Z*V.Z;
	}

	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 float ceVector3D::operator^(const ceVector3D& V) const
	{
		return ((Y*V.Z - Z*V.Y) - (X * V.Z - Z*V.X) + (X * V.Y - Y * V.X));
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	 float ceVector3D::DotProduct(const ceVector3D& A, const ceVector3D& B)
	{
		return A | B;
	}

	 ceVector3D ceVector3D::Cross3(const ceVector3D & V, const ceVector3D & B) 
	{
		return ceVector3D((V.Y*B.Z - V.Z*B.Y) ,(-1*(V.X * B.Z - V.Z*B.X)), (V.X * B.Y - V.Y * B.X));
	}

